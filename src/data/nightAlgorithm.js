// Что делать при ночном пробуждении — зависит от времени и от того,
// сколько прошло после отбоя.
const NIGHT_DONT = 'Ночью нельзя: включать яркий свет, разговаривать, выносить малыша из комнаты и брать в свою кровать (если приучаете к отдельной кроватке).'

export function nightAlgorithm({ hour, hoursSinceBedtime }) {
  if (hour >= 5 && hour < 7) {
    return {
      title: 'Раннее утро (5–7)',
      lines: [
        'Кормление можно, но не выносите малыша из комнаты.',
        'Если проснулся от шума и уснул сам — не вмешивайтесь.',
        NIGHT_DONT
      ]
    }
  }
  if (hoursSinceBedtime != null && hoursSinceBedtime >= 3) {
    return {
      title: 'Ночное кормление',
      lines: [
        'Прошло 3+ часа после отбоя — покормите в полной темноте, без разговоров, и уложите обратно в кроватку.',
        NIGHT_DONT
      ]
    }
  }
  return {
    title: 'Ночное пробуждение',
    lines: [
      'Прошло меньше 3 часов после отбоя — сначала проверьте подгузник на ощупь, не включая свет. Если сухой — покачайте с рукой на животе, не вынимая из кроватки без необходимости.',
      NIGHT_DONT
    ]
  }
}
