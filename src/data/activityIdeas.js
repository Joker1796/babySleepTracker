// Идеи, чем занять малыша в активное бодрствование, по возрасту (месяцы).
export const ACTIVITY_IDEAS = [
  { fromM: 0, toM: 2, ideas: [
    'выкладывание на живот по 1–2 минуты несколько раз',
    'рассматривание вашего лица и контрастных чёрно-белых картинок',
    'спокойные «разговоры», песенки, лёгкий массаж'
  ] },
  { fromM: 2, toM: 4, ideas: [
    'выкладывание на живот — тренируем удержание головы',
    'яркие игрушки, за которыми можно следить и тянуться',
    'потешки, «велосипед» ножками, лёгкая гимнастика'
  ] },
  { fromM: 4, toM: 6, ideas: [
    'хватание и перекладывание игрушек из руки в руку',
    'помогаем осваивать перевороты со спины на живот',
    'игрушки с разной текстурой, погремушки, безопасное зеркальце'
  ] },
  { fromM: 6, toM: 9, ideas: [
    'практика сидения и ползания на полу',
    'пальчиковые игры, «ку-ку», стаканчики и сортеры',
    'первые книжки с плотными страницами'
  ] },
  { fromM: 9, toM: 13, ideas: [
    'практика вставания и первых шагов у опоры',
    'игры с коробками и вкладышами, простые причинно-следственные игрушки',
    'называйте предметы, читайте книжки, показывайте жесты «пока-пока»'
  ] }
]

export function activitiesForAge(months) {
  const found = ACTIVITY_IDEAS.find(a => months >= a.fromM && months < a.toM)
  return (found || ACTIVITY_IDEAS[ACTIVITY_IDEAS.length - 1]).ideas
}
