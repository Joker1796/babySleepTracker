// Советы по укладыванию под место, где сейчас укладывают малыша.
// Персонализируются по профилю ребёнка (aids, feeding).

// Где укладывают малыша — от места зависят советы.
export const SETTLING_LOCATIONS = [
  { id: 'home', label: 'Дома', icon: '🏠' },
  { id: 'walk', label: 'На прогулке', icon: '🚶' },
  { id: 'guests', label: 'В гостях / кафе', icon: '🧳' }
]

// Советы по укладыванию под конкретное место. Персонализируются по профилю.
export function settlingAdviceByLocation(child, location) {
  const aids = child.aids || []
  const breast = child.feeding === 'breast' || child.feeding === 'mixed'
  const has = id => aids.includes(id)

  if (location === 'walk') {
    const a = [
      'Мерное движение коляски укачивает — катите равномерно, без резких остановок и разговоров.',
      'Прикройте от яркого света капюшоном или лёгкой накидкой, но обязательно оставьте доступ воздуха: плотно накрывать коляску нельзя — это духота и перегрев.',
      has('white-noise')
        ? 'Включите привычный белый шум — ровный уличный фон тоже помогает уснуть.'
        : 'Тихо напевайте что-то монотонное; ровный уличный шум работает как белый шум.',
      'Проверьте базовое: малыш не голоден, подгузник сухой, одет по погоде и не перегрет.'
    ]
    if (has('pacifier')) a.push('Предложите соску — в дороге сосание особенно успокаивает.')
    a.push('Дайте время: на свежем воздухе дети засыпают чуть дольше, зато крепко.')
    return a
  }

  if (location === 'guests') {
    const a = [
      'Унесите малыша от шума и большого числа людей в тихий уголок или отдельную комнату.',
      'Воссоздайте кусочек привычной обстановки: свой спальник или плед, знакомая игрушка-компаньон, знакомый запах.',
      'Затемните пространство: капюшон коляски, слинг, ваши руки, приглушённый свет.',
      has('white-noise')
        ? 'Включите привычный белый шум с телефона — знакомый звук сигналит «пора спать».'
        : 'Включите белый шум с телефона — знакомый ровный звук заглушит чужую обстановку.',
      'Проведите сжатую версию домашнего ритуала — те же слова и действия, что и дома.',
      'Возьмите на руки или в слинг и покачайте: в новом месте контакт с вами особенно важен.'
    ]
    if (breast) a.push('Кормление помогает переключиться на сон и успокоиться в непривычной обстановке.')
    else if (has('pacifier')) a.push('Соска поможет малышу успокоиться среди незнакомого окружения.')
    return a
  }

  // Дома — полный набор условий и действий (то, что раньше было в чек-листе)
  const a = [
    has('blackout') ? 'Задёрните блэкаут-шторы — в полной темноте лучше вырабатывается мелатонин.' : 'Затемните комнату как можно сильнее — свет мешает выработке мелатонина.',
    has('white-noise') ? 'Включите белый шум на всё время сна.' : 'Обеспечьте тишину или включите ровный белый шум.',
    'Проверьте условия: 18–22 °C, свежий подгузник, малыш не голоден.',
    'Проведите привычный ритуал в той же последовательности.',
    'Возьмите на руки и покачайте ~10 минут, затем положите сонным: рука на груди, тихое «шшш», лёгкие похлопывания.'
  ]
  if (has('pacifier')) a.push('Предложите соску — ритмичное сосание помогает уснуть.')
  if (breast) a.push('Можно ненадолго приложить к груди, чтобы расслабить, но кладите сонным, а не уснувшим.')
  a.push('Если малыш заплакал — снова возьмите на руки, успокойте и повторите. Несколько подходов — это нормально.')
  return a
}
