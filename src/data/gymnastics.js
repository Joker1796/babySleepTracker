// Подбор комплекса по возрасту (в месяцах). Если точного совпадения нет
// (возраст младше первого или старше последнего комплекса), возвращаем
// ближайший по возрасту, чтобы экран не был пустым.
export function complexForAge(months) {
  const filled = GYMNASTICS.filter(c => c.exercises.length > 0)
  if (!filled.length) return null
  if (months == null) return filled[0]
  const exact = filled.find(c => months >= c.fromM && months < c.toM)
  if (exact) return exact
  const distance = c => (months < c.fromM ? c.fromM - months : months >= c.toM ? months - c.toM : 0)
  return filled.reduce((best, c) => (distance(c) < distance(best) ? c : best))
}
